std::string encode(std::string inpt) {
	string oupt {};

	for(int i = 0; i < inpt.length(); i++) {
		if (EMOJI_ENCODE.find(inpt.at(i)) != EMOJI_ENCODE.end()) // Check if character exists in key
			oupt += EMOJI_ENCODE[inpt.at(i)];
		else
			oupt += inpt.at(i);
	}

	return oupt;
}

